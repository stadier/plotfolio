const N = 256; // words per list

const LAT_STEPS = 16384; // 180° / 16384 ≈ 0.011°
const LNG_STEPS = 32768; // 360° / 32768 ≈ 0.011°

function quantise(lat: number, lng: number): number {
	const latIdx = Math.round(((lat + 90) / 180) * (LAT_STEPS - 1));
	const lngIdx = Math.round(((lng + 180) / 360) * (LNG_STEPS - 1));
	return latIdx * LNG_STEPS + lngIdx;
}

/**
 * Convert coordinates to a three-word PlotWords code.
 * Example: toPlotWords(6.524, 3.379) → "calm.brook.shine"
 */
export function toPlotWords(lat: number, lng: number): string {
	const idx = quantise(lat, lng);
	const w1 = ADJECTIVES[idx % N];
	const w2 = NOUNS[Math.floor(idx / N) % N];
	const w3 = VERBS[Math.floor(idx / (N * N)) % N];
	return `${w1}.${w2}.${w3}`;
}

/**
 * Check whether a string looks like a PlotWords code (word.word.word).
 */
export function isPlotWordsCode(input: string): boolean {
	const parts = input.trim().toLowerCase().split(".");
	if (parts.length !== 3) return false;
	return (
		ADJECTIVES.includes(parts[0]) &&
		NOUNS.includes(parts[1]) &&
		VERBS.includes(parts[2])
	);
}

/**
 * Reverse a PlotWords code back to approximate coordinates.
 * Returns null if the code is invalid.
 */
export function fromPlotWords(
	code: string,
): { lat: number; lng: number } | null {
	const parts = code.trim().toLowerCase().split(".");
	if (parts.length !== 3) return null;

	const i1 = ADJECTIVES.indexOf(parts[0]);
	const i2 = NOUNS.indexOf(parts[1]);
	const i3 = VERBS.indexOf(parts[2]);
	if (i1 === -1 || i2 === -1 || i3 === -1) return null;

	const idx = i1 + i2 * N + i3 * N * N;
	const latIdx = Math.floor(idx / LNG_STEPS);
	const lngIdx = idx % LNG_STEPS;

	const lat = (latIdx / (LAT_STEPS - 1)) * 180 - 90;
	const lng = (lngIdx / (LNG_STEPS - 1)) * 360 - 180;

	return { lat: Math.round(lat * 1e6) / 1e6, lng: Math.round(lng * 1e6) / 1e6 };
}
