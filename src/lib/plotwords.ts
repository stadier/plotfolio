/**
 * PlotWords — a what3words-style location encoding for Plotfolio.
 *
 * Converts a lat/lng pair into a deterministic, human-readable
 * three-word code like "summit.coral.drift".  The same coordinates
 * (rounded to ~11 m precision) always produce the same triplet.
 *
 * Algorithm
 * ---------
 * 1. Quantise lat (±90) into ~8 200 steps and lng (±180) into ~16 400
 *    steps → each step ≈ 0.011° ≈ 1.1 m at the equator (slightly more
 *    than what3words' 3 m, but close enough for property-level use).
 * 2. Combine into a single index  i = latIdx * LNG_STEPS + lngIdx.
 * 3. Map i → (w1, w2, w3) via base conversion over three independent
 *    word lists (each 256 words → 256³ ≈ 16.7 M unique codes, more
 *    than the ~134 M cells, so we use modular wrap which is fine for a
 *    vanity label, not a reversible address).
 */

